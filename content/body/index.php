<ul>
  <li>Are you trying to learn how to build accessible code from scratch?</li>
  <li>Do you have to make an existing website (with a lot of pages and
    components already made) accessible?
  <li>Do you need a component (like a carousel) that is already accessible.</li>
</ul>

<p>If you are asking any of these questions, Enable is for you.</p>

<p>
  There are a <strong>lot</strong> of JavaScript libraries that claim they are
  accessible, but they actually aren't. The Enable Project's goal is to show
  developers (as well as designers
  and UX professionals) how to make accessible components from scratch,
  step-by-step,
  explaining along the way what makes it accessible.
</p>

<h2>What Makes Enable Different?</h2>

<ul>
  <li>
    The Enable Project's components have been tested with a lot of different
    browsers and screen-readers.
  </li>
  <li>
    The components have been <strong>tested on mobile devices</strong> (a lot of
    other components forget
    that
    screen readers on mobile devices work slightly differently to their desktop
    cousins).
  </li>
  <li>
    The Enable Project is focused on UX. It's goal is to help developers make an
    accessible web that is
    usable by everyone.
  </li>
  <li>
    Not only do we want to help developers, but <strong>we want the help from
      developers to contribute their
      accessible code.</strong>
    If you have an accessible component that you don't see here, please consider
    donating by raising a pull
    request.
  </li>
</ul>