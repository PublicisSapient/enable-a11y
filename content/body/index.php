  <div class="homepage__intro--section">
    <h2 class="homepage__intro--header">Learn How To Create Accessible Code</h2>
    <p>
      Whether you are new to accessibility or are a seasoned veteran, Enable's aim to <strong>teach devlopers how to
        make
        something accessible</strong> and to <strong>show why it is.</strong> Developers can walk through the code step
      by step to understand what HTML, CSS and JavaScript are needed to ensure their work "just works" for all users, no
      matter how they navigate the web.
    </p>
  </div>
  <div class="homepage__intro--section">
    <h2 class="homepage__intro--header">Add Accessible Code Quickly To Existing Projects</h2>

    <p>
      The Enable Accessibility Project follows the <a href="https://www.w3.org/TR/using-aria/#rule1">First Rule of
        ARIA</a> when recommending code and encourages the use of native HTML5 functionality whenever possible (as long
      as browser accessibility support is there). However, since we know there are a lot of developers looking to make
      their existing custom widgets accessibile quickly due to time constraints, we also show custom code examples and
      what code features make them work with assistive technology.
    </p>


  </div>
  <div class="homepage__intro--section">
    <h2 class="homepage__intro--header">Use Pre-built Accessible Components</h2>

    <p>
      Enable also includes components that can be included into your own projects via NPM, with instructions how to
      integrate them as Webpack modules, native ES6 Modules or as old school ES4 JavaScript libraries. Accessible
      hamburger menus, carousels and mobile friendly skip links, are only a few examples of prebuilt code you can use in
      your projects today. We also encourage developers to consider contributing their own code to us for the benefit of
      the developer community at large.
  </div>
</div>

<h2>What Makes Enable Different?</h2>

<ul>
  <li>
    The Enable Project's components have been tested with a lot of different
    browsers and screen-readers.
  </li>
  <li>
    The components have been <strong>tested on mobile devices</strong> (a lot of
    other components forget
    that
    screen readers on mobile devices work slightly differently to their desktop
    cousins).
  </li>
  <li>
    The Enable Project is focused on UX. It's goal is to help developers make an
    accessible web that is
    usable by everyone.
  </li>
  <li>
    Not only do we want to help developers, but <strong>we want the help from
      developers to contribute their
      accessible code.</strong>
    If you have an accessible component that you don't see here, please consider
    donating by raising a pull
    request.
  </li>
</ul>